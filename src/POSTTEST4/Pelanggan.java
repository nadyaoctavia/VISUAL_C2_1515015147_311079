    private void teksnoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_teksnoKeyPressed
        // TODO add your handling code here:
         teksno.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e){
            char karakter1 = e.getKeyChar();
            if(!((karakter1 >= '0') && (karakter1 <= '9')) || (karakter1 == '\b')){
                e.consume();
            }
            }
            
}); 
    }//GEN-LAST:event_teksnoKeyPressed

